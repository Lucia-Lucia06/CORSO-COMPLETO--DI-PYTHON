"""+ = addizzione
- = sottrazione
* = moltiplicazione
/ = divisione
// = divisione numeri interi (arrotondamento)
% = percentuale
** = potenza
"""

num1 = 12
num2 = 30

somma = num1 + num2
print(somma)

risultato_sott = num1 - num2 
print(risultato_sott)

resto_mul = num1 * num2 
print(resto_mul)

resto_div = num1 / num2
print(resto_div)

resto_modu = num1 % num2 
print(resto_modu)

resto_nin = num1 // num2
print(resto_nin)

resto_pot = num1 ** num2
print(resto_pot)